export function tr(text, lang = i18n.language) {
  if (!text || typeof text !== 'string') return text;
  const normalized = (lang || '').toLowerCase();
  if (normalized.startsWith('kk') || normalized.startsWith('kz')) {
    return INLINE_KK[text] || text;
  }
  return text;
}

